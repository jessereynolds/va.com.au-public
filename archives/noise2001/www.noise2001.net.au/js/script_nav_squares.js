function sqOn(imageName) {

  if (document.images) {

    document[imageName].src = "http://www.noise2001.net.au/resources/nav-square_ov.gif";

  }



}



function sqOff(imageName) {

  if (document.images) {

    document[imageName].src = "http://www.noise2001.net.au/resources/nav-square.gif";

  }

}







